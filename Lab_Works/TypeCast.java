package Lab_Works;
public class TypeCast {
    public static void main(String[] args) {
        int x = 111;
        long y = x;
        float z = y;
        // Automatic Type Conversions in both cases
        System.out.println("Value of int is ... "+x);
        System.out.println("Value of long is .. "+y);
        System.out.println("Value of float is . "+z);
    }
}
